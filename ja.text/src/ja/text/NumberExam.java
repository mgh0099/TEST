package ja.text;

public class NumberExam {
    public static void main(String[] args) {
        // 숫자 자료형    

        // 1. 정수  2, 100 ,300
            //   int(***)

            //   long (43214321L   숫자 끝에 대문자 L)

        // 2. 실수  3.14
                // double(***)

                // float(3.14543253254543F 숫자 끝에 대문자F)

        int a = 10;
        int b = 5;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
    }
}